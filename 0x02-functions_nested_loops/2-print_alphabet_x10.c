#include "main.h"

/**
 * print_alphabet_x10 - printing it 10 times
 */
void print_alphabet_x10(void)
{
	for (L = 'a'; L <= 'z'; L++)
		_putchar(L);
		_putchar(L);
		_putchar(L);
		_putchar(L);
		_putchar(L);
		_putchar(L);
		_putchar(L);
		_putchar(L);
		_putchar(L);
		_putchar(L);
		_putchar('\n');
}
